package week3;

public class Exercise18 {

	public static void main(String[] args) {
		float a=2.0F, b=1.0F, c;
		c=(float) (a+b);
		System.out.println(c);
		c=(float) (a-b);
		System.out.println(c);
		c=(float) (a*b);
		System.out.println(c);
		c=(float) (a/b);
		System.out.println(c);
		c=(float) (a%b);
		System.out.println(c);
		System.out.println(" ");
		double d=2.0, e=1.0, f;
		f=(double) (d+e);
		System.out.println(f);
		f=(double) (d-e);
		System.out.println(f);
		f=(double) (d*e);
		System.out.println(f);
		f=(double) (d/e);
		System.out.println(f);
		f=(double) (d%e);
		System.out.println(f);
		//In this case, as the numbers have no decimals, the result is the same for both cases, integers and reals.
		
		
	}

}
