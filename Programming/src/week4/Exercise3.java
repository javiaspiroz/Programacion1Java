public class Exercise3 {

	public static void main(String[] args) {
		int a=4;
		float b=13.3F;
		boolean c=false;
		a+=2;
		//The += is an addition operator.
		System.out.println(a);
		a-=3;
		//The -= is a subtraction operator.
		System.out.println(a);
		a*=3;
		System.out.println(a);
		//The *= is a multiplier operator.
		a/=2;
		System.out.println(a);
		//The /= is a division operator.
		a%=4;
		System.out.println(a);
		//The %= is a reminder operator.
		b/=2;
		System.out.println(b);
		//The /= is a division operator.
		c&=true;
		System.out.println(c);
		// The &= is a bitwise AND operator.
		c|=true;
		System.out.println(c);
		// The |= is a bitwise OR operator.
				

	}

}
